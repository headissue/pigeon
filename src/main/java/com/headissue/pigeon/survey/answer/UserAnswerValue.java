package com.headissue.pigeon.survey.answer;


import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;
import java.util.List;

/**
 * piece of information sent by the client in order to answer a question
 */
@XmlRootElement
@XmlAccessorType(XmlAccessType.FIELD)
public class UserAnswerValue {

  @XmlElement(name = "question_id")
  private int questionId;

  private List<String> values;

  public UserAnswerValue() {
  }

  public UserAnswerValue(int questionId, List<String> values) {
    this.questionId = questionId;
    this.values = values;
  }

  public int getQuestionId() {
    return questionId;
  }

  public void setQuestionId(int questionId) {
    this.questionId = questionId;
  }

  public List<String> getValues() {
    return values;
  }

  public void setValues(List<String> values) {
    this.values = values;
  }
}
